export type TrainType = '普通' | '準急' | '急行'
export type DayType = 'weekday' | 'holiday'

export interface Train {
    hour: number
    minute: number
    destination: string
    trainType: TrainType
}

export interface Timetable {
    weekday: Train[]
    holiday: Train[]
}

// フォーマット: "MM行き先"（普通）/ "MM準急:行き先" / "MM急行:行き先"
const h = (hour: number, entries: string[]): Train[] =>
    entries.map((s): Train => {
        const junkyu = s.match(/^(\d+)準急:(.+)$/)
        if (junkyu) return { hour, minute: Number(junkyu[1]), destination: junkyu[2], trainType: '準急' }
        const kyuko = s.match(/^(\d+)急行:(.+)$/)
        if (kyuko) return { hour, minute: Number(kyuko[1]), destination: kyuko[2], trainType: '急行' }
        const futsu = s.match(/^(\d+)(.+)$/)
        if (futsu) return { hour, minute: Number(futsu[1]), destination: futsu[2], trainType: '普通' }
        throw new Error(`無効なデータ: ${s}`)
    })

